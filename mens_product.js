let append_clothing_data = (data)=>{
    let container_clothing_ = document.getElementById("container_to_append");
    container_clothing_.innerHTML = null;
    data.forEach((el)=>{
        let small_div_ = document.createElement("div");
        small_div_.onclick = function(){
            saveProduct_men(el);
        }
        let image_men_product = document.createElement("img");
        image_men_product.src = el.image;
        // open
        image_men_product.addEventListener("mouseover",function(){
            image_men_product.src = el.image_2;
        })
        image_men_product.addEventListener("mouseout",function(){
            image_men_product.src = el.image;
        }) 
        // end

        let text_div_ = document.createElement("div");
        let brand_men = document.createElement("p");
        brand_men.innerText = el.brand;
        brand_men.setAttribute("class","brand_name")
        let title_men = document.createElement("p");
        title_men.innerText = el.product;
        let price_men = document.createElement("p");
        price_men.innerText = "$" + el.price;
        price_men.setAttribute("class","price_quantity");
        text_div_.append(brand_men,title_men,price_men)
        small_div_.append(image_men_product,text_div_);
        container_clothing_.append(small_div_);
    })
}
